add_action('pre_get_posts', function($query) {

    if ( ($query->is_post_type_archive('propostas') || $query->is_tax('tema')) && $query->is_main_query() && !is_admin() ) {
        $query->set('orderby', 'menu_order');
        $query->set('order', 'ASC');
        $query->set('posts_per_page', -1);
    }

});
